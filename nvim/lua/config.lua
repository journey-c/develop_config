local vim_floaterm = function()
    g.floaterm_title = 'terminal'
    g.floaterm_autoclose = 0
    cmd [[
command! -nargs=0 CodeForces :FloatermNew g++ % -std=c++17 -o %< && ./%< < in
command! -nargs=0 CxxRun :FloatermNew g++ % -std=c++17 -o %< && ./%<
command! -nargs=0 ShellRun :FloatermNew sh %
]]
    map('n', '<leader>tn', ':FloatermNew<CR>',   { noremap = true })
end

local lualine = function()
    local custom_gruvbox = require'lualine.themes.gruvbox'
    custom_gruvbox.normal.c.bg = '#112233' -- rgb colors are supported
    require('lualine').setup {
        options = {
            icons_enabled = true,
            theme = custom_gruvbox,
            disabled_filetypes = {},
            component_separators = '|',
            section_separators = {left = '', right = ''}
        },
        sections = {
            lualine_a = {'mode'},
            lualine_b = {'branch', 'diff', {'diagnostics', sources={'nvim_lsp', 'coc'}}},
            lualine_c = {'filename'},
            lualine_x = {'encoding', 'fileformat', 'filetype'},
            lualine_y = {'progress'},
            lualine_z = {'location'}
        },
        inactive_sections = {
            lualine_a = {},
            lualine_b = {},
            lualine_c = {'filename'},
            lualine_x = {'location'},
            lualine_y = {},
            lualine_z = {}
        },
        tabline = {},
        extensions = {}
    }
end

